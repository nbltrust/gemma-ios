import Foundation

@IBDesignable
class AssetsView: EOSBaseView {
    enum Event:String {
        case AssetsViewDidClicked
    }
        
    override func setup() {
        super.setup()
        
        setupUI()
        setupSubViewEvent()
    }
    
    func setupUI() {
        
    }
    
    func setupSubViewEvent() {
    
    }
    
    @objc override func didClicked() {
        self.next?.sendEventWith(Event.AssetsViewDidClicked.rawValue, userinfo: ["data": self.data ?? "", "self": self])
    }
}
